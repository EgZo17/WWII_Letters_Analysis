def get_all_keywords_for_emotion(emotion: str) -> list:
    """
    Возвращает все ключевые слова (но не фразы!) для эмоции.
    Фразы обрабатываются отдельно.
    """
    if emotion not in EMOTION_KEYWORDS:
        return []

    data = EMOTION_KEYWORDS[emotion]
    all_words = []

    # Добавляем только слова из категорий (без фраз!)
    for category in ['nouns', 'verbs', 'adjectives']:
        if category in data:
            all_words.extend(data[category])

    return list(set(all_words))


def get_phrase_list_for_emotion(emotion: str) -> list:
    """
    Возвращает список целых фраз для эмоции.
    """
    if emotion not in EMOTION_KEYWORDS:
        return []

    data = EMOTION_KEYWORDS[emotion]
    return data.get('phrases', [])


def get_all_keywords_flat() -> dict:
    """
    Возвращает словарь {эмоция: [слова_без_фраз]} для быстрого поиска по словам.
    """
    result = {}
    for emotion in EMOTION_KEYWORDS:
        result[emotion] = get_all_keywords_for_emotion(emotion)
    return result

def get_all_phrases() -> dict:
    """
    Возвращает словарь {эмоция: [фразы]} для быстрого поиска по фразам.
    """
    result = {}
    for emotion in EMOTION_KEYWORDS:
        result[emotion] = get_phrase_list_for_emotion(emotion)
    return result
